from typing import List

# @lc code=start
from functools import cache
from heapq import heappop, heappush

def solve_sorted_limits(nums, right_limits):
    num_count = len(nums)
    limit_count = len(right_limits)
    
    assert limit_count <= num_count

    max_heap = []
    total_sum = 0
    
    # We keep a pointer to `nums` to know how many elements we have pushed so far
    current_ptr = 0

    for limit in right_limits:
        
        # Expand Pool: Add all new valid elements from `nums` into the heap We
        # stop when we reach the limit (inclusive) or run out of `nums`
        while current_ptr <= limit and current_ptr < num_count:
            heappush(max_heap, (-nums[current_ptr], current_ptr))
            current_ptr += 1

        assert len(max_heap) >= 0

        val, _ = heappop(max_heap)

        total_sum += -val

    return total_sum

class Solution:
    def maximumScore(self, nums: List[int], s: str) -> int:

        ones = []
        for i in range(len(s)):
            if s[i] == '1':
                ones.append(i)

        return solve_sorted_limits(nums, ones)

